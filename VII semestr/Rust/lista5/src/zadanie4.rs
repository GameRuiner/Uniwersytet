#[allow(dead_code)]
fn my_first_interpreter(code: &str) -> String {
    let mut cell:u8 = 0;

    code.chars().map(|x| 
        match x {
        '+' => { cell = cell.wrapping_add(1); ""},
        '.' => &cell.to_string()[..],
         _  => "",
    }.to_string()).collect()
    
}

